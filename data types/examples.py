# 1 example
x = "Hello World"	                            #datatype is str	
x = 20	                                        #datatype is int	
x = 20.5	                                    #datatype is float	
x = 1j	                                        #datatype is complex	
x = ["apple", "banana", "cherry"]	            #datatype is list	
x = ("apple", "banana", "cherry")	            #datatype is tuple	
x = range(6)	                                #datatype is range	
x = {"name" : "John", "age" : 36}	            #datatype is dict	
x = {"apple", "banana", "cherry"}	            #datatype is set	
x = frozenset({"apple", "banana", "cherry"})	#datatype is frozenset	
x = True	                                    #datatype is bool	
x = b"Hello"	                                #datatype is bytes	
x = bytearray(5)	                            #datatype is bytearray	
x = memoryview(bytes(5))	                    #datatype is memoryview	
x = None	                                    #datatype is NoneType
